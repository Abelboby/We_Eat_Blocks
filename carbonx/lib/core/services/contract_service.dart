  // Use constants from NetworkConstants
  static const String _rpcUrl = NetworkConstants.rpcUrl;
  static const String _contractAddress = NetworkConstants.contractAddress;
  static const int _chainId = NetworkConstants.chainId;

  final Web3Client _client;
  final DeployedContract _contract;

  ContractService()
      : _client = Web3Client(_rpcUrl, http.Client()),
        _contract = DeployedContract(
          ContractAbi.fromJson(_contractAbi, 'CarbonXContract'),
          EthereumAddress.fromHex(_contractAddress),
        );

  // Sell tokens to the contract for ETH
  Future<String> sellTokens({
    required Credentials credentials,
    required BigInt tokenAmount,
  }) async {
    try {
      // Get the function from the contract
      final function = _contract.function('sellTokens');

      // Send the transaction
      final result = await _client.sendTransaction(
        credentials,
        Transaction.callContract(
          contract: _contract,
          function: function,
          parameters: [tokenAmount],
          maxGas: NetworkConstants
              .defaultGasLimit, // Use the constant for gas limit
        ),
        chainId: _chainId,
      );

      debugPrint('Successfully sent sell tokens transaction: $result');
      return result;
    } catch (e) {
      debugPrint('Error selling tokens: $e');
      throw Exception('Failed to sell tokens: $e');
    }
  }

  // Get token balance for an address
  Future<BigInt> getTokenBalance(String address) async {
    try {
      final function = _contract.function('getTokenBalance');
      final params = [EthereumAddress.fromHex(address)];

      final result = await _client.call(
        contract: _contract,
        function: function,
        params: params,
      );

      debugPrint('Token balance for $address: ${result[0]}');
      return result[0] as BigInt;
    } catch (e) {
      debugPrint('Error getting token balance: $e');
      throw Exception('Failed to get token balance: $e');
    }
  }

  // Get citizen token balance for an address
  Future<BigInt> getCitizenTokenBalance(String address) async {
